#include "libft.h"

void	*ft_memcpy(void *dest, void *src, size_t n)
{
	char		*tmp;
	const char	*s;

	*tmp = dest;
	*s = src;
	while (n--)
	{
		*tmp++ = *s++;
	}
	return (dest);
}
