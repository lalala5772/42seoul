#include "libft.h"
//수정필
void	*ft_bzero(void *ptr, size_t	n)
{
	unsigned char	*ch;

	ch = (unsigned char *)ptr;
	while (n--)
	{
		ch = 0;
		*ch++;
	}
	return (ch);
}
