#include "libft.h"

void	ft_bzero(void *p, size_t n)
{
	size_t	i;
	char	*ptr;

	ptr = (char *)p;
	i = 0;
	while (i < n)
	{
		ptr[i] = 0;
		i++;
	}
	return ;
}